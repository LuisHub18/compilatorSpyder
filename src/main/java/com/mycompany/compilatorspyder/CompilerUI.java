    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        jTextArea2.setText(""); // Limpiar el área de texto del scanner

        // Obtener el código del área de texto de programa
        String codigo = jTextArea1.getText();

        // Realizar el análisis léxico&
        try {
            ArrayList<Token> tokens = (ArrayList<Token>) SpiderLexicalAnalyzer.analyze(codigo);

            // Mostrar los tokens en el área de texto del scanner
            for (Token token : tokens) {
                jTextArea2.append(token.getType() + " : " + token.getLexeme() + "\n");
            }

        } catch (LexicalException lexEx) {
            // Capturar y mostrar errores léxicos
            jTextArea2.append("Error léxico: " + lexEx.getMessage());
        }

        // Por ahora, solo mostraremos un mensaje
        JOptionPane.showMessageDialog(null, "Escaneo completado");
    }//GEN-LAST:event_jButton1ActionPerformed

